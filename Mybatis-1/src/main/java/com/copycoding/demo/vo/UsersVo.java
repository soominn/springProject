package com.copycoding.demo.vo;

public class UsersVo {
	private String user_id;
	private String company_partition_code;
	private String dept_code;
	private String user_name_kr;
	
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getCompany_partition_code() {
		return company_partition_code;
	}
	public void setCompany_partition_code(String company_partition_code) {
		this.company_partition_code = company_partition_code;
	}
	public String getDept_code() {
		return dept_code;
	}
	public void setDept_code(String dept_code) {
		this.dept_code = dept_code;
	}
	public String getUser_name_kr() {
		return user_name_kr;
	}
	public void setUser_name_kr(String user_name_kr) {
		this.user_name_kr = user_name_kr;
	}
}
